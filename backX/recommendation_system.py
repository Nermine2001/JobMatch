"""# Phase 1 : Pr√©processing des Donn√©es"""

class DataPreprocessor:
    def __init__(self):
        self.skill_normalizer = {
            "js": "javascript",
            "py": "python",
            "react native": "react",
            "html5": "html",
            "css3": "css",
            "nodejs": "node.js",
            "reactjs": "react",
            "vuejs": "vue",
            "angularjs": "angular",
            "c++": "cpp",
            "c#": "csharp",
            "ai": "artificial intelligence",
            "ml": "machine learning",
            "db": "database",
            "api": "rest api"
        }

    def clean_skills(self, skills_list: List[str]) -> List[str]:
        """Normalise et nettoie la liste des comp√©tences"""

        if not skills_list:
            return []

        cleaned = []
        for skill in skills_list:

            if not skill:
                continue

            # Nettoyage basique
            skill = str(skill).lower().strip()
            skill = re.sub(r'[^\w\s]', '', skill)
            skill = re.sub(r'\s+', ' ', skill)

            # Normalisation
            skill = self.skill_normalizer.get(skill, skill)

            if len(skill) > 1:  # √âviter les comp√©tences trop courtes
                cleaned.append(skill)

        return list(set(cleaned))  # Supprime les doublons

    def normalize_experience(self, years):
        """Normalise l'exp√©rience sur une √©chelle 0-1"""
        if pd.isna(years) or years <= 0:
            return 0.0
        elif years >= 15:
            return 1.0
        else:
            return min(years / 15.0, 1.0)

    def normalize_salary(self, salary, min_market=25000, max_market=150000):
        """Normalise le salaire sur une √©chelle 0-1"""
        if pd.isna(salary) or salary <= min_market:
            return 0.0
        elif salary <= min_market:
            return 0.0
        elif salary >= max_market:
            return 1.0
        else:
            return (salary - min_market) / (max_market - min_market)

    def extract_text_features(self, job_description: str) -> str:
        """Extrait les features textuelles d'une description"""

        if not job_description:
            return ""

        # Supprime HTML, caract√®res sp√©ciaux
        text = re.sub(r'<[^>]+>', '', str(job_description))
        text = re.sub(r'[^\w\s]', ' ', text)
        text = re.sub(r'\s+', ' ', text)
        text = text.lower().strip()

        return text

